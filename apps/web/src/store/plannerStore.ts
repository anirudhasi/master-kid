// Outlook-style planner: per-child scheduled events with flexible time, optional
// recurrence ("apply to N days / weekdays / custom"), reminders, and free per-day
// notes. Reactive cache / local source of truth.

export type EventCategory =
  | 'school' | 'study' | 'homework' | 'activity' | 'sport' | 'music'
  | 'dance' | 'art' | 'coaching' | 'meal' | 'break' | 'free' | 'sleep' | 'other'

export type Repeat = 'none' | 'daily' | 'weekdays' | 'weekly' | 'custom'

export interface PlannerEvent {
  id: string
  childId: string
  title: string
  category: EventCategory
  date: string          // anchor date, yyyy-mm-dd
  startTime: string     // HH:mm
  endTime: string       // HH:mm
  location?: string
  notes?: string
  reminderMinutes?: number | null  // minutes before start; null/undefined = none
  repeat: Repeat
  repeatDays?: number[] // 0=Sun..6=Sat (for custom)
  repeatUntil?: string  // yyyy-mm-dd inclusive; '' = open-ended (capped on read)
  fromTimetable?: boolean // generated by the School Timetable builder (re-appliable)
  createdAt: number
}

